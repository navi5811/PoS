package com.increff.employee.model;

public class ReportInventoryData {

	public int reportInventoryQuantity;
	public String brandName;
	public String brandCategory;

	public int getReportInventoryQuantity() {
		return reportInventoryQuantity;
	}

	public void setReportInventoryQuantity(int reportInventoryQuantity) {
		this.reportInventoryQuantity = reportInventoryQuantity;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getBrandCategory() {
		return brandCategory;
	}

	public void setBrandCategory(String brandCategory) {
		this.brandCategory = brandCategory;
	}

}
